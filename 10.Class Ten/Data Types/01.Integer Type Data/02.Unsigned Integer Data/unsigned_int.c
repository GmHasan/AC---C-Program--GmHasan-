#include<stdio.h>
int main() {
    //Unsigned Short - %hu
    unsigned short us = 41;
    printf("I am Unsigned short - %hu\n", us);

    //Unsigned Int - %u
    unsigned int ui = 4454;
    printf("I am Unsigned int - %u\n", ui);

    //Unsigned Long - %lu
    unsigned long ul = 445454;
    printf("I am Unsigned long - %lu\n", ul);

    //Unsigned Long Long = %llu
    unsigned long long ull = 4454544454;
    printf("I am Unsigned long long - %llu", ull);

    return 0;
}