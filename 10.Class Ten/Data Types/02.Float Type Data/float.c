#include<stdio.h>
int main() {
    //Float - %f
    float f = 489.123f;
    printf("I am Float - %f\n", f);

    //Double - %lf
    double d = 47789.56;
    printf("I am Double - %lf\n", d);

    //Long double - %Lf
    long double ld = 1475701971.1975;
    printf("I am Long double - %Lf", ld);

    return 0;
}